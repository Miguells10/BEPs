'''2. Escreva um programa que pergunte a idade do usuário. Se a idade for 18 anos ou
mais, exiba "Maior de idade". Caso contrário, a saída deve ser "Menor de idade".'''

idade = int(input("Qual sua idade?"))
if idade >= 18:
    print("Voce eh maior de idade")

else:
    print("Voce eh menor de idade")
